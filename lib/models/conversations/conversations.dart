class CustomListItem {
  final String title;
  final String subtitle;
  final String time;
  final String count;
  final bool call;
  final String url;
  final num dash;
  final double space;

  CustomListItem(
      {required this.title,
      required this.call,
      required this.subtitle,
      required this.time,
      required this.count,
      required this.url,
      required this.dash,
      required this.space});
}
